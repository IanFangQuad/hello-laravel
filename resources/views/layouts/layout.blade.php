<script>
    $(function() {});

    function modalMsg(modalId, msgContainerId, redirectUrl) {
        const msg = $(`#${msgContainerId}`).text().trim();

        if (!msg) {
            return;
        }

        const modal = new bootstrap.Modal(document.getElementById(modalId));
        const modelEl = document.getElementById(modalId);

        modelEl.addEventListener('hide.bs.modal', function(event) {
            if (redirectUrl) {
                window.location = redirectUrl;
            }
        })

        modal.show();

    }
</script>
